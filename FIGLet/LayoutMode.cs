namespace FIGLet
{
    /// <summary>
    /// Specifies the layout mode for rendering FIGLet text.
    /// </summary>
    public enum LayoutMode
    {
        /// <summary>
        /// Full size layout mode.
        /// </summary>
        FullSize = -1,

        /// <summary>
        /// Kerning layout mode.
        /// </summary>
        Kerning = 0,

        /// <summary>
        /// Smushing layout mode.
        /// </summary>
        Smushing = 1
    }
}